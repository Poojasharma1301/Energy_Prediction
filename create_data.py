import pandas as pd
import numpy as np

#Create voltage column
Voltage = np.random.uniform(220, 240, size=1000)
data = {'Voltage': Voltage}
df = pd.DataFrame(data)

df.head()

# Create Current Column
Current = np.random.uniform(0.35, 0.50, size=1000)
data1 = {'Current': Current}
df1 = pd.DataFrame(data1)

df1

data=[df,df1]
data=pd.concat(data,axis=1)

data.head()

#Create Power Column
# calculate the power
data['power'] = data['Current'] * data['Voltage']

data.head()

#Create Energy Consumption Column
# define the power factor and time values
power_factor = 0.8
time = 2  # in hours (h)

# calculate the energy consumption
time_sec = time * 3600  # convert time to seconds
data['energy'] = data['power'] * time_sec
data['time_consumed'] = data['energy'] / (power_factor * data['Voltage'] * data['Current'])

data.head()

data.shape

# save the updated dataset to a new file
df.to_csv('Final_data.csv', index=False)

